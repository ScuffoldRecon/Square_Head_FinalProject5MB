using UnityEngine;
using System.Numerics; // ????????????????????????????? (??????????)

public class CurrencyManager : MonoBehaviour
{
    // 1. Singleton Instance ???????????????????????????????
    public static CurrencyManager Instance { get; private set; }

    // 2. ???????????????????????????
    // ??? BigInteger ?????? Gold ??? Silver ????????????????????????????
    // ??? int ?????? Diamond ?????????????????????? Premium ?????????????????
    [Header("Current Currency Values")]
    [SerializeField]
    private BigInteger gold = 0;
    [SerializeField]
    private BigInteger silver = 0;
    [SerializeField]
    private int diamond = 0;

    // 3. Unity Lifecycle - Awake ???????????????? Singleton
    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            // ????? Manager ?????????????????????????? Scene (??????????)
            // DontDestroyOnLoad(gameObject); 
        }
        else
        {
            // ???????????????? Instance ????????????
            Destroy(gameObject);
        }
    }

    // 4. Properties (?????????)
    public BigInteger Gold => gold;
    public BigInteger Silver => silver;
    public int Diamond => diamond;

    // 5. ???????????????????????? (Add)
    // ???????????????????????????????? ????????????????
    public void AddCurrency(int amount, string type)
    {
        if (amount <= 0) return;

        type = type.ToLower();
        switch (type)
        {
            case "gold":
                gold += amount;
                Debug.Log($"Added {amount} Gold. Total Gold: {gold}");
                // *** ??????????? Event ??????????? UI ????????? ***
                break;
            case "silver":
                silver += amount;
                Debug.Log($"Added {amount} Silver. Total Silver: {silver}");
                // *** ??????????? Event ??????????? UI ????????? ***
                break;
            case "diamond":
                diamond += amount;
                Debug.Log($"Added {amount} Diamond. Total Diamond: {diamond}");
                // *** ??????????? Event ??????????? UI ????????? ***
                break;
            default:
                Debug.LogWarning($"Unknown currency type: {type}");
                break;
        }
    }

    // 6. ?????????????????????? (Spend)
    // ??????????????????????
    public bool SpendCurrency(int cost, string type)
    {
        if (cost <= 0)
        {
            Debug.LogWarning("Cost must be greater than 0.");
            return false;
        }

        type = type.ToLower();

        switch (type)
        {
            case "gold":
                if (gold >= cost)
                {
                    gold -= cost;
                    Debug.Log($"Spent {cost} Gold. Remaining Gold: {gold}");
                    // *** ??????????? Event ??????????? UI ????????? ***
                    return true;
                }
                Debug.Log("Not enough Gold.");
                return false;
            case "silver":
                if (silver >= cost)
                {
                    silver -= cost;
                    Debug.Log($"Spent {cost} Silver. Remaining Silver: {silver}");
                    // *** ??????????? Event ??????????? UI ????????? ***
                    return true;
                }
                Debug.Log("Not enough Silver.");
                return false;
            case "diamond":
                if (diamond >= cost)
                {
                    diamond -= cost;
                    Debug.Log($"Spent {cost} Diamond. Remaining Diamond: {diamond}");
                    // *** ??????????? Event ??????????? UI ????????? ***
                    return true;
                }
                Debug.Log("Not enough Diamond.");
                return false;
            default:
                Debug.LogWarning($"Unknown currency type: {type}");
                return false;
        }
    }

    // 7. ??????????????????????????????????
    public bool CanAfford(int cost, string type)
    {
        type = type.ToLower();
        switch (type)
        {
            case "gold":
                return gold >= cost;
            case "silver":
                return silver >= cost;
            case "diamond":
                return diamond >= cost;
            default:
                return false;
        }
    }

    // *** ????????: ???????????????????????????????? ??????????????????? Save/Load ???? PlayerPrefs ???????? Save ????? ***
